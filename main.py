import colorama
from colorama import init, Fore, Back, Style


# -----------------------------
# 1. ІНІЦІАЛІЗАЦІЯ БІБЛІОТЕКИ
# -----------------------------
# init() необхідна для коректної роботи кольорів у Windows
init()


# -----------------------------
# 2. ІНТРОСПЕКЦІЯ БІБЛІОТЕКИ
# -----------------------------
print("=== Інтроспекція модуля colorama ===")
print(dir(colorama))

print("\n=== Інтроспекція Fore ===")
print(dir(Fore))

print("\n=== Інтроспекція Back ===")
print(dir(Back))

print("\n=== Інтроспекція Style ===")
print(dir(Style))


# -----------------------------
# 3. АНАЛІЗ ОСНОВНИХ КОМПОНЕНТІВ
# -----------------------------

# Fore — відповідає за колір тексту
print(Fore.RED + "Це червоний текст")
print(Fore.GREEN + "Це зелений текст")
print(Fore.BLUE + "Це синій текст")

# Back — відповідає за колір фону
print(Back.YELLOW + "Текст з жовтим фоном")
print(Back.CYAN + "Текст з блакитним фоном")

# Style — відповідає за стиль тексту
print(Style.BRIGHT + "Яскравий текст")
print(Style.DIM + "Затемнений текст")

# RESET_ALL — скидає всі стилі та кольори
print(Style.RESET_ALL + "Звичайний текст після скидання")


